public class Parrot extends Bird{
    public Parrot(String name, int age, int legs, boolean canFly, boolean isVegeterian) {
        super(name, age, legs, canFly, isVegeterian);
    }

    @Override
    public void sings() {
        super.sings();
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
